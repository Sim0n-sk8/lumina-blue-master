"use client";

import localFont from "next/font/local";
import "./globals.css";
import { SiteSettingsProvider, useSiteSettings } from './context/SiteSettingsContext';
import { useState, useEffect } from "react";
import Head from "next/head";

const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900",
});

const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900",
});

function LayoutContent({ children }) {
  const { siteSettings, isLoading } = useSiteSettings();
  const [title, setTitle] = useState("Lumina Blue");
  const [bannerImage, setBannerImage] = useState("");

  useEffect(() => {
    if (siteSettings?.name) setTitle(siteSettings.name);

    // Pick the first banner or based on display_order
    const banner = siteSettings?.banners?.[0];
    if (banner?.bannerImg) setBannerImage(banner.bannerImg);
  }, [siteSettings]);

  return (
    <html lang="en">
      <Head>
        <title>{isLoading ? "Loading..." : title}</title>
        <meta name="description" content="Generated by create next app" />
        
        {/* Open Graph image */}
        {bannerImage && <meta property="og:image" content={bannerImage} />}
      </Head>
      
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased flex flex-col min-h-screen w-full`}>
        <main className="flex-grow w-full relative">
          {children}
        </main>
      </body>
    </html>
  );
}

export default function RootLayout({ children }) {
  return (
    <SiteSettingsProvider>
      <LayoutContent>{children}</LayoutContent>
    </SiteSettingsProvider>
  );
}
